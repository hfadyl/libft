#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*k;

	k = (unsigned char*)b;
	if (len)
	{
		while (len--)
		{
			*k++ = c;
		}
	}
	return (b);
}
